def birthdayCakeCandles(candles):
    dic = {}
    maxN = 0
    for x in candles:
        if x in dic:
            dic.update({x: dic[x]+1})
        else:
            dic.update({x: 1})
        if x>maxN:
            maxN = x
    return (dic[maxN])

